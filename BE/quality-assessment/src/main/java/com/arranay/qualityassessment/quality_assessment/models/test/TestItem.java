package com.arranay.qualityassessment.quality_assessment.models.test;

public class TestItem {
    private String id;
    private int count;

    public TestItem(String id, int count) {
        this.id = id;
        this.count = count;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
